package day07_conditionals;

public class Chars {
	
	public static void main(String[] args) {
		
		
		char letter = 'a'; // a--> 97
		letter ++; // 98 (b)
		
		
		
		int ascii = letter;
		
		
		System.out.println(letter);
		
		
		
		System.out.println(ascii);
		
		
		char myChar = 002;
		System.out.println(myChar);
		
		
		--myChar;
		System.out.println(myChar);
		
		
		char symbol = 'C';
		System.out.println(symbol);
		
		char ch = 'd';
		System.out.println(ch);
		
	}

}
